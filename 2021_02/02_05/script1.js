// 변수를 설정하고 산술연산을 실행
// var(상수, 변수 구분없이 사용) 변수 설정 키워드
// let(일반변수설정), const(상수변수설정) 변수설정 키워드 - ECMA Script 6
    var a = 100; // 변수 a를 버킷(양동이)이고 거기에 담는 숫자 100을 변수 값이라 함.
        a = 50;  // 변수 a의 값은 50으로 바뀐다. var 키워드는 재사용이 가능함
    var b = 300;
    var c = a + b; // 변수 c(350)=a(50)+b(300) 
        console.log(c+'=a+b ECMA Script 5표현');  //문자열과 변수 상수등 연결 연산자 +
        console.log(`${c}=a+b ECMA Script 6표현`) 

        console.log(a>b); // flase
        console.log(a<b); // true
        console.log(a==b); // flase
