// 변수 : 값이 변경되서 사용되는 것

// 변수의 규칙
// - 첫글자가 영문으로 시작(대소문자 구분함)
// - 네이밍 규칙 중 카멜케이스를 따른다.
// - 공백은 절대불가함.
// - 컴퓨터에서 사용되는 키워드는 사용불가함.
// - 첫글자를 영문으로 쓰고 나머지는 숫자 혼용사용가능.
// - 첫글자 예외문자 _ (언더바), $ (달러)
// - 숫자형은 인용부호가 없는 순수한 숫자를 의미함
// - 문자형은 인용부호가 있는 숫자 또는 문자, 특수문자 등을 의미함
// - 컴퓨터 입력상에서 입력된 숫자는 문자형으로 인식한다.
// - 부울(참, 거짓) : true==(1, yes), false==(0, no)
// - 논리연산 : AND(논리곱) , OR(논리합) , NOT(논리부정)
// - 산술연산 : +(더하기) -(빼기) *(곱하기) /(나누기) %(나머지)
// - 비교연산 : >(크다) <(작다) >=(크거나 같다) <=(작거나 같다)
//              ==(같다) ===(논리까지 같다) !=(같지않다) !==(논리까지 같지않다)

// - 증감연산 : ++ , -- , +=1 , +=2 , ...
    var a = 0;
        a++;    // 1증가 a = a+1 또는 a+=1
        a+=2;   // 2증가 a = a+2

        a-=2;   // 2감소 a = a-2


